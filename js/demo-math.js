function demoAddition() {
    // Addition

    var sum;

    sum = 10 + 20;

    document.write("10 added to 20 is: ");
    document.write(sum); // this will output 30
}

function demoSubtraction() {
    // Subtraction

    var exampleResults;

    exampleResults = 100 - 60;

    document.write("60 subtracted from 100 is: ");

    document.write(exampleResults); // this will output 40
}

function demoMultiplication() {
    // Multiplication

    var productOfTwoNumbers;

    productOfTwoNumbers = 100 * 20;

    document.write("100 multiplied by 20 is: ");

    document.write(productOfTwoNumbers); // this will output 2000
}

function demoDivision() {
    // Division

    var sampleQuotient;

    sampleQuotient = 100 / 20;

    document.write("100 divided by 20 is: ");

    document.write(sampleQuotient); // this will output 5
}

function demoMathWithVariables() {
    // Math with variables

    var firstNumber;

    var secondNumber;

    var sumTotal;

    firstNumber = 100;

    secondNumber = 200;

    sumTotal = firstNumber + secondNumber;

    document.write(firstNumber);

    document.write(" added to ");

    document.write(secondNumber);

    document.write(" is: ");

    document.write(sumTotal);
}
